//! IVC Accumulator — Groth16 recursive verifier for FL round proofs.
//!
//! ## Fixes in this version
//!
//! ### Fix 2: Persistent IVC VK (Critical)
//! The IVC summary circuit's trusted setup is done ONCE and the VK is
//! persisted to `ivc_vk.bin` + `ivc_pk.bin`.  Subsequent `accumulate` calls
//! load these keys from disk.  The VK is public and can be pinned on-chain,
//! so anyone can verify the IVC proof independently.
//!
//! ### Fix 6: N-independent circuit (Significant)
//! The circuit is padded to a fixed capacity `MAX_ROUNDS = 64`.  All
//! witnesses beyond the actual N rounds are filled with zero-value
//! "dummy" records that satisfy every constraint trivially.  The constraint
//! structure is completely fixed regardless of N:
//!
//!   • Hash chain: enforce new_hash[i] == prev_hash[i+1] for ALL MAX_ROUNDS-1
//!     pairs — dummy slots satisfy this trivially (both are Fr::zero()).
//!   • Final hash: a private selector vector `sel[0..MAX_ROUNDS]` with
//!     exactly one bit set at position n-1 selects the last real new_hash.
//!     Constraints: each sel[i] ∈ {0,1}, Σsel[i] == 1,
//!     Σ(sel[i] * new_hash[i]) == final_hash.
//!   • Proof chain hash: Poseidon over all MAX_ROUNDS commits (always fixed).
//!
//! This means one trusted setup covers any N ≤ MAX_ROUNDS.
//!
//! ## Security model
//!
//! The summary proof is NOT a full recursive SNARK (no in-circuit Groth16
//! pairing check — that requires a Pasta/BLS12-381 cycle).  Instead it
//! proves hash-chain integrity via Poseidon:
//!   - Forging any round breaks the Poseidon chain hash.
//!   - Forging the summary proof requires breaking Groth16 soundness.
//! The VK is now fixed and public, so a fresh VK cannot be generated by a
//! malicious prover (Fix 2).

